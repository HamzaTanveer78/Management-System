package classes;

public class SubjectResult {

	
	
	String id;
	String name;
	String subjectname;
	String midmarks;
	String finalmarks;
	String sessionalmarks;
	String totalmarks;
	String gpa;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSubjectname() {
		return subjectname;
	}
	public void setSubjectname(String subjectname) {
		this.subjectname = subjectname;
	}
	public String getMidmarks() {
		return midmarks;
	}
	public void setMidmarks(String midmarks) {
		this.midmarks = midmarks;
	}
	public String getFinalmarks() {
		return finalmarks;
	}
	public void setFinalmarks(String finalmarks) {
		this.finalmarks = finalmarks;
	}
	public String getSessionalmarks() {
		return sessionalmarks;
	}
	public void setSessionalmarks(String sessionalmarks) {
		this.sessionalmarks = sessionalmarks;
	}
	public String getTotalmarks() {
		return totalmarks;
	}
	public void setTotalmarks(String totalmarks) {
		this.totalmarks = totalmarks;
	}
	public String getGpa() {
		return gpa;
	}
	public void setGpa(String gpa) {
		this.gpa = gpa;
	}
	
	
	
	
	
	
	
}
